'''Traduza os seguintes laços do seguinte trecho de código, escritos com o comando for, em laços escritos com o comando while equivalentes, mantendo a mesma sequência'''

for count in range(1, 55):
   print(count)

count = 1
while count < 55:
   print(count)
   count += 1

for count in range(50):
   print(count)

count = 0
while count < 50:
   print(count)
   count += 1

for count in range(50, 0, -2):
   print(count)

count = 50
while count < 0:
   print(count)
   count -= 2